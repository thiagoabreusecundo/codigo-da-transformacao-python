# 1. Usando a função print() para exibir mensagens e valores

print("Iniciando a exploração do Python...")

# Exibindo uma string (texto)
print("Esta é uma mensagem de texto (string).")

# Exibindo um número inteiro (int)
print(42)

# Exibindo um número decimal (float)
print(5.99)

# 2. Usando a função type() para identificar tipos de dados

# Armazenando valores em variáveis para testar seus tipos
texto = "Olá, Vocaçao!"
numero_inteiro = 100
numero_decimal = 7.5
valor_logico = True

# Usando print() e type() para mostrar o tipo de cada variável
print("\n--- Verificando os Tipos de Dados ---")
print("O tipo da variável 'texto' é:", type(texto))
print("O tipo da variável 'numero_inteiro' é:", type(numero_inteiro))
print("O tipo da variável 'numero_decimal' é:", type(numero_decimal))
print("O tipo da variável 'valor_logico' é:", type(valor_logico))

# 3. Teste rápido de operações

soma = 5 + 3
print("\nA soma de 5 + 3 é:", soma)
print("O tipo do resultado da soma é:", type(soma))
